package org.example;

public class Habitat {
    private int habitatId;
    private String habitatName;
    private String habitatDescription;
    private int regionId;

    // Getters and Setters
    public int getHabitatId() { return habitatId; }
    public void setHabitatId(int habitatId) { this.habitatId = habitatId; }

    public String getHabitatName() { return habitatName; }
    public void setHabitatName(String habitatName) { this.habitatName = habitatName; }

    public String getHabitatDescription() { return habitatDescription; }
    public void setHabitatDescription(String habitatDescription) { this.habitatDescription = habitatDescription; }

    public int getRegionId() { return regionId; }
    public void setRegionId(int regionId) { this.regionId = regionId; }
}


